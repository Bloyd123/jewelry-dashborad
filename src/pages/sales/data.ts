export const getSaleById = (saleId: string): Sale | undefined => {
  return dummySales.find(sale => sale._id === saleId)
}

export const getSalesByStatus = (status: string): Sale[] => {
  return dummySales.filter(sale => sale.status === status)
}

export const getSalesByPaymentStatus = (paymentStatus: string): Sale[] => {
  return dummySales.filter(sale => sale.payment.paymentStatus === paymentStatus)
}

export const getSalesByCustomer = (customerId: string): Sale[] => {
  return dummySales.filter(sale => sale.customerId === customerId)
}

export const getTodaySales = (): Sale[] => {
  const today = new Date().toISOString().split('T')[0]
  return dummySales.filter(sale => sale.saleDate.startsWith(today))
}

export const getPendingSales = (): Sale[] => {
  return dummySales.filter(
    sale => sale.status === 'pending' || sale.status === 'draft'
  )
}

export const getUnpaidSales = (): Sale[] => {
  return dummySales.filter(
    sale =>
      sale.payment.paymentStatus === 'unpaid' ||
      sale.payment.paymentStatus === 'partial'
  )
}

export const getOverdueSales = (): Sale[] => {
  const now = new Date()
  return dummySales.filter(
    sale =>
      sale.payment.dueDate &&
      new Date(sale.payment.dueDate) < now &&
      (sale.payment.paymentStatus === 'unpaid' ||
        sale.payment.paymentStatus === 'partial')
  )
}

